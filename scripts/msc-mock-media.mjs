#!/usr/bin/env node
/**
 * Generate mock media placeholders for Payload sandbox (examples/nextjs-payload/media/mock/ only)
 * Usage: npm run msc:mock:media
 * Powered by the MSC Media Engine
 */
import './lib/msc-load-env.mjs';
import './lib/msc-node-version-guard.mjs';

import { mkdirSync, writeFileSync } from 'node:fs';
import { join } from 'node:path';
import { MSC_PROJECT_ROOT } from './lib/msc-load-env.mjs';

const MOCK_DIR = join(MSC_PROJECT_ROOT, 'examples/nextjs-payload/media/mock');

const PLACEHOLDERS = [
  {
    name: 'msc-mock-hero.svg',
    content: `<svg xmlns="http://www.w3.org/2000/svg" width="800" height="450" viewBox="0 0 800 450">
  <rect width="800" height="450" fill="#1c1c1c"/>
  <text x="400" y="225" fill="#ffffff" font-family="sans-serif" font-size="24" text-anchor="middle">MSC Mock Hero</text>
</svg>`,
  },
  {
    name: 'msc-mock-thumbnail.svg',
    content: `<svg xmlns="http://www.w3.org/2000/svg" width="320" height="180" viewBox="0 0 320 180">
  <rect width="320" height="180" fill="#252525"/>
  <text x="160" y="95" fill="#b3b3b3" font-family="sans-serif" font-size="14" text-anchor="middle">Mock Thumb</text>
</svg>`,
  },
  {
    name: 'README.md',
    content: `# Mock media (MSC)

Placeholder assets for Payload sandbox development. Generated by \`npm run msc:mock:media\`.
Do not commit live uploads — this directory holds fixtures only.
`,
  },
];

mkdirSync(MOCK_DIR, { recursive: true });

for (const { name, content } of PLACEHOLDERS) {
  const dest = join(MOCK_DIR, name);
  writeFileSync(dest, content, 'utf8');
  console.log(`[msc:mock:media] Wrote examples/nextjs-payload/media/mock/${name}`);
}

console.log('[msc:mock:media] Mock media pipeline complete');
